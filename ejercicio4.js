//Calcular el perímetro de un cuadrado. Los valores deben de ser capturados con promt y mostrados con alertas.
let ladoCuadrado = prompt("Inserte lado de cuadrado");
console.log(ladoCuadrado);
let perimetroCuadrado = parseFloat(ladoCuadrado) * 4;
alert("El perímetro del cuadrado es igual a: " + perimetroCuadrado);
